package Seller;

public class Seller {
    private int codSeller;
    private String rg;
    private String name;
    private int expYear;
    private String phone;

    public int getCodSeller() {
        return codSeller;
    }

    public void setCodSeller(int codSeller) {
        this.codSeller = codSeller;
    }

    public String getRg(){
        return rg;
    }

    public void setRg(){
        this.rg = rg;
    }

    public String getName(){
        return name;
    }

    public void setName(){
        this.name = name;
    }

    public int getExpYear(){
        return expYear;
    }

    public void setExpYear(){
        this.expYear = expYear;
    }

    public String getPhone(){
        return phone;
    }

    public void setPhone(){
        this.phone = phone;
    }


}
